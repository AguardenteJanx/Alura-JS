// == -> comparação implícita
const numero = 900;
const texto = "900";

console.log(numero == texto);
console.log(numero === texto); //Retorna false, pois os três === validam tanto o valor quanto o tipo da var.

//typeof -> é responsável por dizer qual o tipo da var
 console.log(typeof numero);
 console.log(typeof texto);

// == só compara o valor
// === compara o valor e o tipo de dados. 
// As boas práticas dizem que devemos usar os três iguais e conersões explícitas

//Conversão explíita
